a = 20
b = 10

#Arithmetic Operators (+, -, *, /, %, **, //)
print (a + b)  # Addition
print (a - b)  # Subtraction
print (a * b)  # Multiplication
print (a / b)  # Division
print (a % b)  # Modulus
print (a ** 2) # Exponentiation
print (a // b) # Floor Division 

# Comparison Operators (>, <, ==, !=, >=, <=)
print (a > b)  # Greater than
print (a < b)  # Less than
print (a == b) # Equal to
print (a != b) # Not equal to
print (a >= b) # Greater than or equal to  
print (a <= b) # Less than or equal to

# Identity Operators (is, is not)
print (a is b)     # Returns True if both variables are the same object
print (a is not b) # Returns True if both variables are not the same object

c = None
print (c is None) # Returns True if c is None

#Logical Operators (and, or, not)
x = True
y = False
print (x and y) # Logical AND
print (x or y)  # Logical OR
print (not x)   # Logical NOT

#Membership Operators (in, not in)
my_list = [1, 2, 3, 4, 5]
print (3 in my_list)    # Returns True if 3 is in my_list
print (6 not in my_list) # Returns True if 6 is not in my_list
